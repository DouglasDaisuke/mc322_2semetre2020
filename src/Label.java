
public enum Label {
	 TO_DO,
	 DOING,
	 DONE;
	
	private String rotulo;
	private String cor;
	
	public String getRotulo() {
		return rotulo;
	}
	
	public void setRotulo(String rotulo) {
		this.rotulo = rotulo;
	}
	
	public String getCor() {
		return cor;
	}
	 
	public void setCor(String cor) {
		this.cor = cor;
	}
}
